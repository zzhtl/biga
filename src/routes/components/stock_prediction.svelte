<script lang="ts">
    import { onMount } from 'svelte';
    import { invoke } from '@tauri-apps/api/core';
    import { confirm } from '@tauri-apps/plugin-dialog';
    
    let stockCode = "";
    let selectedModelName = "";
    let daysToPredict = 5;
    let useExistingModel = true;
    let isTraining = false;
    let isPredicting = false;
    let errorMessage = "";
    
    // ÂÆö‰πâÊé•Âè£
    interface ModelInfo {
        id: string;
        name: string;
        stock_code: string;
        created_at: number;
        model_type: string;
        features: string[];
        target: string;
        prediction_days: number;
        accuracy: number;
    }
    
    interface TechnicalIndicatorValues {
        rsi: number;
        macd_histogram: number;
        kdj_j: number;
        cci: number;
        obv_trend: number;
        // Êñ∞Â¢ûMACDÂíåKDJÂ≠óÊÆµ
        macd_dif: number;
        macd_dea: number;
        kdj_k: number;
        kdj_d: number;
        macd_golden_cross: boolean;
        macd_death_cross: boolean;
        kdj_golden_cross: boolean;
        kdj_death_cross: boolean;
        kdj_overbought: boolean;
        kdj_oversold: boolean;
    }
    
    interface Prediction {
        target_date: string;
        predicted_price: number;
        predicted_change_percent: number;
        confidence: number;
        trading_signal?: string;
        signal_strength?: number;
        technical_indicators?: TechnicalIndicatorValues;
    }
    
    interface TrainingLog {
        epoch: number;
        loss: string;
        timestamp: string;
        accuracy?: string;
    }
    
    interface ModelComparisonItem {
        name: string;
        type: string;
        accuracy: number;
        created_at: string;
    }
    
    interface ChartData {
        labels: string[];
        datasets: Array<{
            label: string;
            data: number[];
            borderColor: string;
            backgroundColor: string;
            fill?: boolean;
            tension?: number;
            yAxisID?: string;
        }>;
    }
    
    // Êñ∞Â¢ûÔºöÊúÄÊñ∞ÁúüÂÆûÊï∞ÊçÆÊé•Âè£
    interface LastRealData {
        date: string;
        price: number;
        change_percent: number;
    }
    
    // Êñ∞Â¢ûÔºöÈ¢ÑÊµãÁªìÊûúËøîÂõûÊé•Âè£
    interface PredictionResult {
        predictions: Prediction[];
        last_real_data?: {
            date: string;
            price: number;
            change_percent: number;
        };
    }
    
    // ‰ΩøÁî®Á±ªÂûã
    let modelList: ModelInfo[] = [];
    let predictions: Prediction[] = [];
    let modelAccuracy: number | null = null;
    let lastRealData: LastRealData | null = null; // Êñ∞Â¢ûÔºöÊúÄÊñ∞ÁúüÂÆûÊï∞ÊçÆ
    
    // Ê®°ÂûãËÆ≠ÁªÉÂèÇÊï∞
    let newModelName = "Ê®°Âûã-" + new Date().toISOString().slice(0, 10);
    let modelType = "candle_mlp"; // ÈªòËÆ§‰ΩøÁî®CandleÁöÑMLPÊ®°Âûã
    let lookbackDays = 180; // ‰øÆÊîπ‰∏∫180Â§©ÂéÜÂè≤Êï∞ÊçÆ
    let trainTestSplit = 0.8;
    let features = ["close", "volume", "change_percent", "ma_trend", "price_position", "volatility", "rsi_signal", "macd_momentum", "ma5", "ma10", "ma20", "rsi", "macd", "bollinger", "stochastic_k", "stochastic_d", "momentum", "kdj_k", "kdj_d", "kdj_j", "cci", "obv", "macd_dif", "macd_dea", "macd_histogram"];
    let epochs = 100; // ËÆ≠ÁªÉËΩÆÊï∞
    let batchSize = 32; // ÊâπÂ§ÑÁêÜÂ§ßÂ∞è
    let learningRate = 0.001; // Â≠¶‰π†Áéá
    let dropout = 0.2; // DropoutÁéá
    let advancedOptions = false; // ÊòØÂê¶ÊòæÁ§∫È´òÁ∫ßÈÄâÈ°π

    let trainingProgress = 0;
    let trainingLogs: TrainingLog[] = [];
    let showTrainingLogs = false;
    let modelComparison: ModelComparisonItem[] = [];
    let showModelComparison = false;
    let predictionChart: ChartData | null = null;

    onMount(async () => {
        try {
            // Â¶ÇÊûúÁî®Êà∑ÈÄâÊã©‰∫ÜËÇ°Á•®‰ª£Á†ÅÔºåÂ∞ùËØïÂä†ËΩΩÊ®°ÂûãÂàóË°®
            if (stockCode) {
                await loadModelList();
            }
        } catch (error) {
            errorMessage = `Âä†ËΩΩÂ§±Ë¥•: ${error}`;
        }
    });

    async function loadModelList() {
        if (!stockCode) return;
        
        try {
            const models: ModelInfo[] = await invoke('list_stock_prediction_models', { symbol: stockCode });
            modelList = models;
            if (modelList.length > 0) {
                selectedModelName = modelList[0].name;
            }
        } catch (error) {
            errorMessage = `Âä†ËΩΩÊ®°ÂûãÂàóË°®Â§±Ë¥•: ${error}`;
            modelList = [];
        }
    }

    // ËÆ≠ÁªÉËøõÂ∫¶ÁõëÊéß
    async function simulateTrainingProgress() {
        trainingProgress = 0;
        trainingLogs = [];
        showTrainingLogs = true;
        
        const progressInterval = setInterval(() => {
            if (trainingProgress < 90) {
                trainingProgress += Math.random() * 15;
                trainingLogs = [...trainingLogs, {
                    epoch: Math.floor(trainingProgress / 90 * epochs),
                    loss: (1.0 - trainingProgress / 100).toFixed(4),
                    timestamp: new Date().toLocaleTimeString()
                }];
            }
        }, 500);
        
        return progressInterval;
    }

    async function trainModel() {
        if (!stockCode) {
            errorMessage = "ËØ∑ÂÖàËæìÂÖ•ËÇ°Á•®‰ª£Á†Å";
            return;
        }

        isTraining = true;
        errorMessage = "";
        trainingProgress = 0;
        
        // ÂºÄÂßãËøõÂ∫¶Ê®°Êãü
        const progressInterval = await simulateTrainingProgress();
        
        try {
            // ËÆ°ÁÆóËÆ≠ÁªÉÊó•ÊúüËåÉÂõ¥ - ËÄÉËôëAËÇ°ËäÇÂÅáÊó•Âõ†Á¥†
            const endDate = new Date().toISOString().slice(0, 10);
            // ‰ΩøÁî®180Â§©ËÆ≠ÁªÉÊï∞ÊçÆ + 30Â§©ËäÇÂÅáÊó•ÁºìÂÜ≤Êúü = 210Â§©ÊÄªËåÉÂõ¥
            const totalDays = lookbackDays + 30; // 180 + 30 = 210Â§©
            const startDateObj = new Date(Date.now() - totalDays * 24 * 60 * 60 * 1000);
            const startDate = startDateObj.toISOString().slice(0, 10);

            console.log(`üìÖ ËÆ≠ÁªÉÊï∞ÊçÆËåÉÂõ¥: ${startDate} Âà∞ ${endDate} (${totalDays}Â§©ÔºåÂê´ËäÇÂÅáÊó•ÁºìÂÜ≤)`);

            const trainRequest = {
                stock_code: stockCode,
                model_name: newModelName,
                start_date: startDate,
                end_date: endDate,
                features: features,
                target: "close",
                prediction_days: daysToPredict,
                model_type: modelType,
                epochs: epochs,
                batch_size: batchSize,
                learning_rate: learningRate,
                dropout: dropout,
                train_test_split: trainTestSplit
            };

            const result = await invoke<{metadata: ModelInfo, accuracy: number}>('train_candle_model', { request: trainRequest });
            
            clearInterval(progressInterval);
            trainingProgress = 100;
            
            const metadata = result.metadata;
            modelAccuracy = result.accuracy;
            
            // Ê∑ªÂä†ËÆ≠ÁªÉÂÆåÊàêÊó•Âøó
            trainingLogs = [...trainingLogs, {
                epoch: epochs,
                loss: "ËÆ≠ÁªÉÂÆåÊàê",
                timestamp: new Date().toLocaleTimeString(),
                accuracy: (modelAccuracy * 100).toFixed(2) + "%"
            }];
            
            await loadModelList();
            useExistingModel = true;
            alert(`Ê®°ÂûãËÆ≠ÁªÉÊàêÂäü: ${metadata.name}, ÂáÜÁ°ÆÁéá: ${(modelAccuracy * 100).toFixed(2)}%`);
            
            // Ëá™Âä®Âä†ËΩΩÊ®°ÂûãÂØπÊØîÊï∞ÊçÆ
            await loadModelComparison();
            
        } catch (error) {
            clearInterval(progressInterval);
            errorMessage = `ËÆ≠ÁªÉÂ§±Ë¥•: ${error}`;
        } finally {
            isTraining = false;
        }
    }

    // Âä†ËΩΩÊ®°ÂûãÊÄßËÉΩÂØπÊØîÊï∞ÊçÆ
    async function loadModelComparison() {
        if (!stockCode) return;
        
        try {
            const models = await invoke('list_stock_prediction_models', { symbol: stockCode }) as ModelInfo[];
            modelComparison = models.map((model: ModelInfo) => ({
                name: model.name,
                type: model.model_type,
                accuracy: model.accuracy * 100,
                created_at: new Date(model.created_at < 1000000000000 ? model.created_at * 1000 : model.created_at).toLocaleString('zh-CN', {
                    year: 'numeric',
                    month: '2-digit',
                    day: '2-digit',
                    hour: '2-digit',
                    minute: '2-digit'
                })
            }));
            showModelComparison = true;
        } catch (error) {
            console.error("Âä†ËΩΩÊ®°ÂûãÂØπÊØîÊï∞ÊçÆÂ§±Ë¥•:", error);
        }
    }

    // ÁîüÊàêÈ¢ÑÊµãÂõæË°®Êï∞ÊçÆ
    function generatePredictionChart(predictions: Prediction[]) {
        if (!predictions || predictions.length === 0) {
            console.log("Ê≤°ÊúâÈ¢ÑÊµãÊï∞ÊçÆÔºåÊó†Ê≥ïÁîüÊàêÂõæË°®");
            return;
        }
        
        console.log("ÁîüÊàêÈ¢ÑÊµãÂõæË°®ÔºåÊï∞ÊçÆÈáè:", predictions.length);
        
        const chartData: ChartData = {
            labels: predictions.map((p: Prediction) => {
                const date = new Date(p.target_date);
                return `${date.getMonth() + 1}/${date.getDate()}`;
            }),
            datasets: [{
                label: 'È¢ÑÊµã‰ª∑Ê†º',
                data: predictions.map((p: Prediction) => p.predicted_price),
                borderColor: 'rgb(79, 70, 229)',
                backgroundColor: 'rgba(79, 70, 229, 0.1)',
                fill: true,
                tension: 0.4
            }, {
                label: 'ÁΩÆ‰ø°Â∫¶ (%)',
                data: predictions.map((p: Prediction) => p.confidence * 100),
                borderColor: 'rgb(34, 197, 94)',
                backgroundColor: 'rgba(34, 197, 94, 0.1)',
                yAxisID: 'y1',
                fill: false
            }]
        };
        
        predictionChart = chartData;
        console.log("È¢ÑÊµãÂõæË°®Êï∞ÊçÆÂ∑≤ÁîüÊàê:", predictionChart);
    }

    async function predictStock() {
        if (!stockCode) {
            errorMessage = "ËØ∑ÂÖàËæìÂÖ•ËÇ°Á•®‰ª£Á†Å";
            return;
        }
        
        if (!selectedModelName && useExistingModel) {
            errorMessage = "ËØ∑ÂÖàÈÄâÊã©Ê®°ÂûãÊàñËÆ≠ÁªÉÊñ∞Ê®°Âûã";
            return;
        }
        
        isPredicting = true;
        errorMessage = "";
        predictionChart = null; // ÈáçÁΩÆÂõæË°®Êï∞ÊçÆ
        
        try {
            const request = {
                stock_code: stockCode,
                model_name: useExistingModel ? selectedModelName : null,
                prediction_days: daysToPredict,
                use_candle: true
            };
            
            console.log("ÂèëÈÄÅÈ¢ÑÊµãËØ∑Ê±Ç:", request);
            const result = await invoke<Prediction[] | PredictionResult>('predict_with_candle', { request });
            console.log("Êî∂Âà∞È¢ÑÊµãÁªìÊûú:", result);
            
            // Â§ÑÁêÜËøîÂõûÁªìÊûúÔºåÊèêÂèñÈ¢ÑÊµãÊï∞ÊçÆÂíåÊúÄÊñ∞ÁúüÂÆûÊï∞ÊçÆ
            if (result) {
                if (Array.isArray(result)) {
                    // ÊóßÊ†ºÂºèÔºåÂè™ËøîÂõûÈ¢ÑÊµãÊï∞ÁªÑ
                    predictions = result;
                } else if ('predictions' in result && Array.isArray(result.predictions)) {
                    // Êñ∞Ê†ºÂºèÔºåÂåÖÂê´È¢ÑÊµãÂíåÊúÄÊñ∞ÁúüÂÆûÊï∞ÊçÆ
                    predictions = result.predictions;
                    if (result.last_real_data) {
                        lastRealData = {
                            date: result.last_real_data.date,
                            price: result.last_real_data.price,
                            change_percent: result.last_real_data.change_percent
                        };
                    }
                }
            } else {
                predictions = [];
                lastRealData = null;
            }
            
            // ÁîüÊàêÂõæË°®Êï∞ÊçÆ
            if (predictions && predictions.length > 0) {
                generatePredictionChart(predictions);
                console.log("ÂõæË°®Êï∞ÊçÆÁîüÊàêÂÆåÊàê:", predictionChart);
            } else {
                console.warn("È¢ÑÊµãÁªìÊûú‰∏∫Á©∫ÔºåÊó†Ê≥ïÁîüÊàêÂõæË°®");
            }
            
        } catch (error) {
            console.error("È¢ÑÊµãÂ§±Ë¥•:", error);
            errorMessage = `È¢ÑÊµãÂ§±Ë¥•: ${error}`;
            predictions = [];
            lastRealData = null;
            predictionChart = null;
        } finally {
            isPredicting = false;
        }
    }
    
    async function handleStockCodeChange() {
        await loadModelList();
    }
    
    async function deleteModel(modelId: string) {
        // ‰ΩøÁî® Tauri ÂØπËØùÊ°ÜËøõË°åÁ°ÆËÆ§
        const confirmed = await confirm('Á°ÆÂÆöË¶ÅÂà†Èô§Ê≠§Ê®°ÂûãÂêóÔºü', { title: 'Âà†Èô§Ê®°Âûã' });
        if (!confirmed) {
            return;
        }
        try {
            await invoke('delete_stock_prediction_model', { modelId });
            await loadModelList();
            alert('Ê®°ÂûãÂà†Èô§ÊàêÂäü');
        } catch (error) {
            errorMessage = `Âà†Èô§Â§±Ë¥•: ${error}`;
        }
    }
    
    // Áî®‰∫éÈáçÊñ∞ËÆ≠ÁªÉÈÄâÂÆöÊ®°Âûã
    async function retrainModel(modelId: string, modelName: string) {
        isTraining = true;
        errorMessage = "";
        
        try {
            await invoke('retrain_candle_model', { 
                modelId,
                epochs: epochs,
                batchSize: batchSize,
                learningRate: learningRate
            });
            alert(`Ê®°Âûã ${modelName} ÈáçÊñ∞ËÆ≠ÁªÉÊàêÂäü`);
            await loadModelList();
        } catch (error) {
            errorMessage = `ÈáçÊñ∞ËÆ≠ÁªÉÂ§±Ë¥•: ${error}`;
        } finally {
            isTraining = false;
        }
    }

    // Ëé∑ÂèñÊ®°ÂûãËØÑ‰º∞‰ø°ÊÅØ
    async function evaluateModel(modelId: string) {
        try {
            const result = await invoke<{accuracy: number, confusion_matrix: any}>('evaluate_candle_model', { modelId });
            const accuracy = result.accuracy;
            const confusion_matrix = result.confusion_matrix;
            
            alert(`Ê®°ÂûãËØÑ‰º∞ÁªìÊûú:\nÂáÜÁ°ÆÁéá: ${(accuracy * 100).toFixed(2)}%\nÊ∑∑Ê∑ÜÁü©Èòµ: ${JSON.stringify(confusion_matrix)}`);
        } catch (error) {
            errorMessage = `ËØÑ‰º∞Â§±Ë¥•: ${error}`;
        }
    }
    
    // ÂàáÊç¢È´òÁ∫ßÈÄâÈ°πÊòæÁ§∫
    function toggleAdvancedOptions() {
        advancedOptions = !advancedOptions;
    }
</script>

